// Problem No: 50
// Problem Name: Pow(x, n) (Medium)
// Problem Link: https://leetcode.com/problems/powx-n
// Time:  O(1)
// Space: O(1)

#include <bits/stdc++.h>
#include <iostream>
using namespace std;

class Solution
{
public:
    double myPow(double x, int n)
    {
        return pow(x, n);
    }
};